    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if (controladorFormularioNetbook.verificarCampos(this)) {
            Netbook netbookNueva;
            String windowsKey = "xxxxxxxxxxxxxxxxxxxxxxxxx".toUpperCase();
            String officeKey = "xxxxxxxxxxxxxxxxxxxxxxxxx".toUpperCase();
            String codigoActivador = "xxxxxxxx";
            String marca = txtMarca.getText().toUpperCase();
            String modelo = txtModelo.getText().toUpperCase();
            String serie = txtNumSerie.getText().toUpperCase();
            String idHardware = txtIdHardware.getText().toUpperCase();
            
            if (controladorFormularioNetbook.ingresaKeyActivador(txtWinKey.getText())) {
                windowsKey = txtWinKey.getText().toUpperCase();
            }
            if (controladorFormularioNetbook.ingresaKeyActivador(txtOfficeKey.getText())) {
                officeKey = txtOfficeKey.getText().toUpperCase();
            }
            if (controladorFormularioNetbook.ingresaKeyActivador(txtCodigoActivador.getText())) {
                codigoActivador = txtCodigoActivador.getText().toUpperCase();
            }
            switch (tipoOperacion) {
                case 'a':
                    netbookNueva = new Netbook(serie.toUpperCase(), marca.toUpperCase(), Boolean.FALSE, windowsKey.toUpperCase(), officeKey.toUpperCase(), modelo.toUpperCase(), idHardware.toUpperCase(), codigoActivador.toUpperCase());
                    if (!controladorNetbook.verificarExistencia(txtNumSerie)) {
                        controladorNetbook.agregarNetbook(netbookNueva);
                        controladorNetbook.setNetbook(netbook);
                        frmPrincipal.getControladorFrmPrincipal().getFrmUsuario(frmPrincipal).getControladorNetbook().setNetbook(netbookNueva);
                        //actualizamos las listas
                        controladorNetbook.actualizarTblNetbookPrincipal(frmPrincipal.getControladorFrmPrincipal().getFrmUsuario(frmPrincipal).getTblNetbook(), tipoOperacion, netbookNueva);
                        try {
                            controladorNetbook.actualizarTblNetbookPrincipal(frmPrincipal.getControladorFrmPrincipal().getFrmNetbookPrincipal(frmPrincipal).getTblNetbook(), tipoOperacion, netbookNueva);
                        } catch (NullPointerException ex) {
                            
                        }
                        //actualizar anuncios
                        frmPrincipal.getControladorFrmPrincipal().getFrmUsuario(frmPrincipal).getControladorFormularioUsuario().actualizarCantidadNetbookOsciosas(frmPrincipal.getControladorFrmPrincipal().getFrmUsuario(frmPrincipal));
                        //mensaje de agregacion
                        JOptionPane.showMessageDialog(this, "La Netbook se agrego correctamente");
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Esta netbook ya existe en la Base De Datos");
                    }
                    break;
                case 'm':
                    netbook.setIdHardware(idHardware);
                    netbook.setMarca(marca);
                    netbook.setModelo(modelo);
                    netbook.setOfficeKey(officeKey);
                    netbook.setWindowsKey(windowsKey);
                    netbook.setCodigoActivador(codigoActivador);
                    controladorNetbook.actualizarDatosNetbook(netbook);
                    controladorNetbook.actualizarTblNetbookPrincipal(frmPrincipal.getControladorFrmPrincipal().getFrmUsuario(frmPrincipal).getTblNetbook(), tipoOperacion, netbook);
                    JOptionPane.showMessageDialog(this, "La Netbook se modifico correctamente");
                    dispose();
                    break;
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Verifique que los datos ingresados sean correctos");
        }

    }//GEN-LAST:event_btnAceptarActionPerformed

