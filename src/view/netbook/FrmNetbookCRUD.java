    private void txtMarcaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMarcaKeyTyped
        // TODO add your handling code here:
        String marca = txtModelo.getText();
        for (Character c : ListaCaracteresNoValidos.LISTA) {
            if (evt.getKeyChar() == c) {
                evt.consume();
                controladorFormularioNetbook.mostrarLabelInfo(lblInfoMarca);
            } else {
                controladorFormularioNetbook.ocultarLabelInfo(lblInfoMarca);
            }
        }
        if (marca.length() >= 20) {
            evt.consume();
            controladorFormularioNetbook.mostrarLabelInfo(lblInfoMarca);
        } else {
            controladorFormularioNetbook.ocultarLabelInfo(lblInfoMarca);
        }
    }//GEN-LAST:event_txtMarcaKeyTyped

